example = 'Автоматизация'
print(example[0]) # Вывод первого символа строки
print(example[-1]) # Вывод последнего символа строки
print(example[6:]) # Вывод второй половины строки (с нечётным количеством символов)
print(example[::-1]) # Вывод всех символов строки в обратном порядке
print(example[1::2]) # Вывод каждого второго символа строки